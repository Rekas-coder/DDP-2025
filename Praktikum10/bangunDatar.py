import math

# Rumus Luas Persegi
def persegi(sisi):
    hasil = pow(sisi,2)
    return hasil

# Rumus Luas Persegi Panjang
def persegipanjang(p,l):
    hasil = p * l
    return hasil

# Rumus Luas Segitiga
def segitiga(alas,tinggi):
    return alas * tinggi * 1/2

# Rumus Lingkaran
def lingkaran(r):
    hasil = 22/7 * pow(r,2)
    return hasil

# Rumus Jajar Genjang
def jajargenjang(alas, tinggi):
    return alas * tinggi
   
